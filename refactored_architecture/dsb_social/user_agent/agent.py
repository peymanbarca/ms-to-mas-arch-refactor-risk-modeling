def build_register_agent(
    redis_client: redis_lib.Redis,
    mongo_col,
) -> any:
    """Build and compile the RegisterUser LangGraph agent."""
    graph = StateGraph(RegisterAgentState)

    graph.add_node("reason_hash_password", make_reason_hash_node())
    graph.add_node("validate_hash",        make_validate_hash_node())
    graph.add_node("persist_user",         make_persist_user_node(redis_client, mongo_col))

    graph.set_entry_point("reason_hash_password")
    graph.add_edge("reason_hash_password", "validate_hash")
    graph.add_edge("validate_hash",        "persist_user")
    graph.add_edge("persist_user",         END)

    return graph.compile()


def build_login_agent(
    redis_client: redis_lib.Redis,
    mongo_col,
) -> any:
    """Build and compile the Login LangGraph agent."""
    graph = StateGraph(LoginAgentState)

    graph.add_node("fetch_user",             make_fetch_user_node(redis_client, mongo_col))
    graph.add_node("reason_verify_password", make_reason_verify_node())
    graph.add_node("validate_decision",      make_validate_decision_node())
    graph.add_node("issue_token",            make_issue_token_node())

    graph.set_entry_point("fetch_user")
    graph.add_edge("fetch_user",             "reason_verify_password")
    graph.add_edge("reason_verify_password", "validate_decision")
    graph.add_edge("validate_decision",      "issue_token")
    graph.add_edge("issue_token",            END)

    return graph.compile()