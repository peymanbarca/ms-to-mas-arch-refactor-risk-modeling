"""
USER AGENT - Two Graph Topologies

RegisterUser graph:
    START
      |
      v
  [reason_hash_password]   LLM — given (password, salt), reason through the
      |                    SHA-256(password + salt) computation and return
      |                    the 64-char hex digest.
      v
  [validate_hash]          Deterministic guard — verify the LLM result is
      |                    exactly 64 lowercase hex chars; if not, fall back
      |                    to hashlib.sha256 reference implementation.
      v
  [persist_user]           Deterministic — MongoDB insert_one + Redis cache.
      v
     END

Login graph:
    START
      |
      v
  [fetch_user]             Deterministic — Redis / MongoDB lookup by username.
      v
  [reason_verify_password] LLM — given (password, salt, stored_hash), reason
      |                    whether SHA-256(password + salt) == stored_hash.
      |                    Returns {"match": true|false}.
      v
  [validate_decision]      Deterministic guard — must be boolean; if malformed,
      |                    fall back to hashlib.verify_password reference.
      v
  [issue_token]            Deterministic — generate and return JWT.
      v
     END

GetUserId / ComposeCreator / ComposeCreatorWithUserId:
  No LLM — pure deterministic Redis/MongoDB lookup.
  Implemented directly in the handler (not via LangGraph).

Key Design Decisions
--------------------
- Thrift interface UNCHANGED (all 6 methods).
- MongoDB schema, Redis key layout UNCHANGED.
- password.py is kept as the deterministic fallback reference.
- jwt_helper.py is unchanged — JWT generation is deterministic.
- Token metrics tracked per operation.
"""

import json
import logging
import re
import asyncio
import threading
from typing import TypedDict, Optional, Dict, Any

from langgraph.graph import StateGraph, END
from langchain_ollama import ChatOllama

from pymongo import MongoClient
from pymongo.errors import DuplicateKeyError
import redis as redis_lib

from .password   import generate_salt, hash_password, verify_password
from .jwt_helper import generate_token

logger = logging.getLogger("user-agent")

# ---------------------------------------------------------------------------
# LLM
# ---------------------------------------------------------------------------

llm = ChatOllama(model="llama3.2:3b", temperature=0.0, reasoning=False)

# ---------------------------------------------------------------------------
# Redis cache key prefixes (identical to original handler.py)
# ---------------------------------------------------------------------------

_KEY_USERNAME = "username:"   # username  → user_id  (str)
_KEY_USER_ID  = "userid:"     # user_id   → JSON user doc


# ===========================================================================
# State definitions
# ===========================================================================

class RegisterAgentState(TypedDict):
    # Inputs
    req_id:      int
    first_name:  str
    last_name:   str
    username:    str
    password:    str
    user_id:     int
    salt:        str        # pre-generated by gather_inputs

    # LLM output
    llm_password_hashed: Optional[str]   # 64-char hex from LLM

    # Final (validated)
    password_hashed: Optional[str]       # confirmed 64-char hex

    # Metrics
    total_input_tokens:  int
    total_output_tokens: int
    total_llm_calls:     int
    fallback_used:       bool


class LoginAgentState(TypedDict):
    # Inputs
    req_id:    int
    username:  str
    password:  str
    secret:    str
    jwt_expiry: int

    # After fetch_user
    user_doc:  Optional[Dict]   # full user document from DB

    # LLM output
    llm_match: Optional[bool]   # True/False from LLM

    # Final
    verified:  Optional[bool]   # confirmed boolean
    token:     Optional[str]    # JWT string

    # Metrics
    total_input_tokens:  int
    total_output_tokens: int
    total_llm_calls:     int
    fallback_used:       bool


# ===========================================================================
# Helpers
# ===========================================================================

def _parse_json(text: str) -> Optional[dict]:
    try:
        m = re.search(r"\{.*\}", text, re.DOTALL)
        if m:
            return json.loads(m.group(0))
    except Exception as exc:
        logger.warning("JSON parse error: %s — raw: %r", exc, text[:300])
    return None


def _is_valid_hash(h: Any) -> bool:
    """A valid SHA-256 hex digest is exactly 64 lowercase hex chars."""
    return (
        isinstance(h, str)
        and len(h) == 64
        and re.fullmatch(r"[0-9a-f]{64}", h) is not None
    )


