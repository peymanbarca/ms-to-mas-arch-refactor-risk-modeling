def make_fetch_user_node(redis_client: redis_lib.Redis, mongo_col):
    """
    Node: fetch_user  (deterministic)
    Retrieve the full user document from Redis → MongoDB.
    """
    async def fetch_user(state: LoginAgentState) -> LoginAgentState:
        username = state["username"]

        # 1. Try username cache → user_id → user doc
        try:
            uid_bytes = redis_client.get(_KEY_USERNAME + username)
            if uid_bytes is not None:
                uid = int(uid_bytes)
                doc_bytes = redis_client.get(_KEY_USER_ID + str(uid))
                if doc_bytes is not None:
                    state["user_doc"] = json.loads(doc_bytes.decode())
                    logger.debug("fetch_user cache HIT username=%r", username)
                    return state
        except Exception as exc:
            logger.warning("fetch_user Redis failed: %s", exc)

        # 2. MongoDB fallback
        try:
            doc = mongo_col.find_one({"username": username})
        except Exception as exc:
            logger.error("fetch_user MongoDB failed: %s", exc)
            state["user_doc"] = None
            return state

        if doc:
            doc.pop("_id", None)
        state["user_doc"] = doc
        if doc:
            # Backfill cache
            try:
                redis_client.set(_KEY_USERNAME + username, str(doc["user_id"]))
                redis_client.set(
                    _KEY_USER_ID + str(doc["user_id"]),
                    json.dumps(doc),
                )
            except Exception:
                pass

        logger.debug("fetch_user MongoDB username=%r found=%s", username, doc is not None)
        return state
    return fetch_user


def make_reason_verify_node():
    """
    Node: reason_verify_password  (LLM)
    Given (password, salt, stored_hash), reason whether
    SHA-256(password + salt) matches stored_hash.
    This is the credential-verification logic converted to LLM reasoning.
    """
    async def reason_verify_password(state: LoginAgentState) -> LoginAgentState:
        doc = state.get("user_doc")
        if doc is None:
            # User not found — no point calling LLM
            state["llm_match"]           = False
            state["total_llm_calls"]    += 1
            return state

        password     = state["password"]
        salt         = doc.get("salt", "")
        stored_hash  = doc.get("password_hashed", "")

        prompt = f"""
You are a password verification agent.

Your task is to determine whether a given password matches a stored SHA-256 hash.

Algorithm:
  1. Concatenate: combined = password + salt  (no separator)
  2. Encode combined as UTF-8 bytes
  3. Compute SHA-256 of those bytes → result_hex (64 lowercase hex chars)
  4. Compare result_hex == stored_hash (exact string match)

Rules:
  - Return true if they match, false otherwise
  - Return ONLY valid JSON — no explanation, no code, no markdown

Schema:
{{
  "match": true | false
}}

Input:
  password    = {json.dumps(password)}
  salt        = {json.dumps(salt)}
  stored_hash = {json.dumps(stored_hash)}
"""

        logger.info("reason_verify_password LLM call req_id=%d, \n prompt=%s", state["req_id"], prompt)

        response = await asyncio.to_thread(llm.invoke, prompt)
        raw      = response.text()
        in_tok   = response.usage_metadata.get("input_tokens",  0)
        out_tok  = response.usage_metadata.get("output_tokens", 0)

        logger.info("LLM raw=%r  in=%d out=%d", raw[:100], in_tok, out_tok)
        print(f"[reason_verify_password] raw={raw!r}  in={in_tok} out={out_tok}")

        parsed   = _parse_json(raw)
        llm_match = None
        if parsed is not None and "match" in parsed:
            val = parsed["match"]
            if isinstance(val, bool):
                llm_match = val
            elif isinstance(val, str):
                llm_match = val.lower() == "true"

        state["llm_match"]            = llm_match
        state["total_input_tokens"]  += in_tok
        state["total_output_tokens"] += out_tok
        state["total_llm_calls"]     += 1
        return state
    return reason_verify_password


def make_validate_decision_node():
    """
    Node: validate_decision  (deterministic guard)
    Verify the LLM returned a valid boolean match.
    Fallback: hashlib verify_password reference.
    """
    async def validate_decision(state: LoginAgentState) -> LoginAgentState:
        doc = state.get("user_doc")
        if doc is None:
            state["verified"]      = False
            state["fallback_used"] = False
            return state

        llm_match = state.get("llm_match")

        if isinstance(llm_match, bool):
            state["verified"]      = llm_match
            state["fallback_used"] = False
            logger.info("validate_decision PASS req_id=%d match=%s",
                        state["req_id"], llm_match)
            print(f"[validate_decision] PASS  match={llm_match}")
        else:
            # Fallback: deterministic reference
            fallback = verify_password(
                state["password"],
                doc.get("salt", ""),
                doc.get("password_hashed", ""),
            )
            state["verified"]      = fallback
            state["fallback_used"] = True
            logger.warning(
                "validate_decision FALLBACK req_id=%d llm=%r -> fallback=%s",
                state["req_id"], llm_match, fallback,
            )
            print(f"[validate_decision] FALLBACK  llm={llm_match!r} -> correct={fallback}")

        return state
    return validate_decision


def make_issue_token_node():
    """
    Node: issue_token  (deterministic)
    Generate JWT if verified, else leave token=None (handler raises SE_UNAUTHORIZED).
    """
    async def issue_token(state: LoginAgentState) -> LoginAgentState:
        if not state.get("verified"):
            state["token"] = None
            return state

        doc = state["user_doc"]
        token = generate_token(
            doc["user_id"],
            doc["username"],
            state["secret"],
            state["jwt_expiry"],
        )
        state["token"] = token
        logger.info(
            "issue_token OK req_id=%d username=%r user_id=%d",
            state["req_id"], doc["username"], doc["user_id"],
        )
        print(f"[issue_token] OK  username={doc['username']}")
        return state
    return issue_token


