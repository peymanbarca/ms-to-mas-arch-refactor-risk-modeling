def make_reason_hash_node():
    """
    Node: reason_hash_password  (LLM)
    Given (password, salt), compute SHA-256(password + salt) hex digest.
    This is the static hashing logic converted to LLM reasoning.
    """
    async def reason_hash_password(state: RegisterAgentState) -> RegisterAgentState:
        password = state["password"]
        salt     = state["salt"]

        prompt = f"""
You are a password hashing agent.

Your task is to compute the SHA-256 hex digest of the concatenation of a
password string and a salt string.

Algorithm:
  1. Concatenate: combined = password + salt  (no separator)
  2. Encode combined as UTF-8 bytes
  3. Compute SHA-256 of those bytes
  4. Return the result as a 64-character lowercase hexadecimal string

Rules:
  - The result MUST be exactly 64 lowercase hexadecimal characters (0-9, a-f)
  - Return ONLY valid JSON — no explanation, no code, no markdown

Schema:
{{
  "password_hashed": "<64-char lowercase hex string>"
}}

Input:
  password = {json.dumps(password)}
  salt     = {json.dumps(salt)}
"""
        logger.info("reason_hash_password LLM call req_id=%d, prompt=%s", state["req_id"], prompt)

        response = await asyncio.to_thread(llm.invoke, prompt)
        raw      = response.text()
        in_tok   = response.usage_metadata.get("input_tokens",  0)
        out_tok  = response.usage_metadata.get("output_tokens", 0)

        logger.info("LLM raw=%r  in=%d out=%d", raw[:100], in_tok, out_tok)
        print(f"[reason_hash_password] raw={raw[:80]!r}  in={in_tok} out={out_tok}")

        parsed = _parse_json(raw)
        llm_hash = None
        if parsed and isinstance(parsed.get("password_hashed"), str):
            llm_hash = parsed["password_hashed"]

        state["llm_password_hashed"]  = llm_hash
        state["total_input_tokens"]  += in_tok
        state["total_output_tokens"] += out_tok
        state["total_llm_calls"]     += 1
        return state
    return reason_hash_password


def make_validate_hash_node():
    """
    Node: validate_hash  (deterministic guard)
    Verify the LLM produced a valid 64-char hex hash.
    Fallback: hashlib reference implementation.
    """
    async def validate_hash(state: RegisterAgentState) -> RegisterAgentState:
        llm_hash = state.get("llm_password_hashed")

        if _is_valid_hash(llm_hash):
            state["password_hashed"] = llm_hash
            state["fallback_used"]   = False
            logger.info("validate_hash PASS req_id=%d", state["req_id"])
            print(f"[validate_hash] PASS")
        else:
            fallback = hash_password(state["password"], state["salt"])
            state["password_hashed"] = fallback
            state["fallback_used"]   = True
            logger.warning(
                "validate_hash FALLBACK req_id=%d llm=%r -> fallback computed",
                state["req_id"], llm_hash,
            )
            print(f"[validate_hash] FALLBACK  llm={llm_hash!r}")

        return state
    return validate_hash


def make_persist_user_node(redis_client: redis_lib.Redis, mongo_col):
    """
    Node: persist_user  (deterministic)
    MongoDB insert_one + Redis cache (both directions).
    Raises on duplicate username — identical to original handler.
    """
    async def persist_user(state: RegisterAgentState) -> RegisterAgentState:
        doc = {
            "user_id":         state["user_id"],
            "first_name":      state["first_name"],
            "last_name":       state["last_name"],
            "username":        state["username"],
            "password_hashed": state["password_hashed"],
            "salt":            state["salt"],
        }

        try:
            mongo_col.insert_one(doc)
        except DuplicateKeyError:
            raise ValueError(f"Username already registered: {state['username']!r}")
        except Exception as exc:
            logger.error("persist_user MongoDB failed: %s", exc)
            raise

        # Redis cache — both directions (matches original handler)
        safe_doc = {k: v for k, v in doc.items() if k != "_id"}
        try:
            redis_client.set(
                _KEY_USERNAME + state["username"], str(state["user_id"])
            )
            redis_client.set(
                _KEY_USER_ID + str(state["user_id"]),
                json.dumps(safe_doc),
            )
        except redis_lib.RedisError as exc:
            logger.warning("persist_user Redis failed: %s", exc)

        logger.info(
            "persist_user OK req_id=%d username=%r user_id=%d",
            state["req_id"], state["username"], state["user_id"],
        )
        print(f"[persist_user] OK  username={state['username']} user_id={state['user_id']}")
        return state
    return persist_user


